         write(nmcgo1,100)ion_mcgo,(rgrid_mcgo(j),j=1,njmcgo)
         write (nmcgo1,101)ion_mcgo,(en(j,ion_mcgo),j=1,njmcgo)
  100    format(2x,'rni(1,',i1,') = ',5(2x,1pe14.8))
  101    format(2x,'eni(1,',i1,') = ',5(2x,1pe14.8))
