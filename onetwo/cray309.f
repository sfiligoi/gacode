               write(nmcgo1,151)i,j,fwall(i,j),i,j,bneut(i,j)
             end do
          end do
  150     format (2x,'bpow_onetwo(',i1,',',i1,') =' , 1pe12.6,
     .            2x,'fap_onetwo(',i1,',',i1,') ='  , 1pe12.6)
