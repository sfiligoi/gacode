      write (nmcgo1,131)(ti(j),j=1,njmcgo)
  130 format(2x,'rti = ',(5(2x,1pe14.8)))
  131 format(2x,'ti = ',(5(2x,1pe14.8)))
      write(nmcgo1,'(''     tib1 = '',1pe14.8)') ti(njmcgo) ! in SOL
