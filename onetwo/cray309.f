        if (itimav .eq. 1)  dflux(j) = dflux(j) + ssawe(j)
      end do
c
c --- get the electron flux on the half grid:
c
      call flxcal (dflux, drr, hcap, nj, r, ra, fluxe)
      return
c
      end


